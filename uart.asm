    SECTION UART
    PUBLIC _SendData
    PUBLIC _uart_command
    PUBLIC _uart_buffer

_SendData:
    UART_TX_PORT  EQU 0xE0
    UART_RX_PORT  EQU 5179

    SendATCommand:
        LD HL, _uart_command
    loop:
        LD A, (HL)
        CP 0
        RET Z
        LD BC, UART_TX_PORT
        OUT (C), A
        INC HL
        JP loop

    WaitResponse:
        LD HL, _uart_buffer
        LD BC, UART_TX_PORT
        IN A, (C)
        BIT 0, A
        JP Z, WaitResponse
    waitloop:
        LD BC, UART_RX_PORT
        IN A, (C)
        CP $0D
        RET Z
        CP $0A
        RET Z
        LD (HL),A
        INC HL
        JP waitloop

